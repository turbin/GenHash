static unsigned int __time33_hash(const char* strIn, size_t nMaxSizeOfStr=-1){			
		UN_USED(nMaxSizeOfStr);																
		unsigned int hash=0x5381;															
		char* p = const_cast<char*>(strIn);													
		while(*p != '\0'){																
			hash += ((hash<<5) + (*p++));													
		}																					
		return hash;																		
	}																						

	char* tolang(const char* tiptext, int nIdxofOtherLang){																				
		unsigned int _hash = __time33_hash(tiptext);																					
																																		
		int idx = 0;																													
		for(;idx!=SIZE_OF_TABLE;idx++){																									
																																		
			if(_hash != MultiLANG[idx].hash)continue;																					
																																		
			return (char*) ((*MultiLANG[idx].gpszTrText[nIdxofOtherLang])?MultiLANG[idx].gpszTrText[nIdxofOtherLang]:tiptext);			
		}																																
																																		
		return (char*)tiptext;																											
	}																																	
